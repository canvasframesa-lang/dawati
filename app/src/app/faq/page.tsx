export default function FaqPage() {
  // Flatten all Q/A pairs for FAQPage JSON-LD so Google can render
  // them as rich results in SERP.
  const faqJsonLd = faqLd(
    SECTIONS.flatMap((sec) => sec.items.map(([q, a]) => ({ q: q!, a: a! }))),
  );

  return (
    <PageShell>
      <script
        type="application/ld+json"
        // biome-ignore lint/security/noDangerouslySetInnerHtml: trusted static schema
        dangerouslySetInnerHTML={{ __html: JSON.stringify(faqJsonLd) }}
      />
      <PageBanner
        eyebrow="الأسئلة الشائعة"
        emoji="❓"
        title="كل ما تحتاج معرفته"
        subtitle="إجابات تفصيلية على كل سؤال محتمل — قبل الطلب، وبعد التسليم."
      />
      <PageContainer>
        <div className="space-y-12 max-w-3xl mx-auto">
        {SECTIONS.map((sec) => (
          <section key={sec.title}>
            <H2>{sec.title}</H2>
            <div className="space-y-3">
              {sec.items.map(([q, a]) => (
                <details
                  key={q}
                  className="group rounded-2xl p-5 bg-white border border-[var(--color-line)] hover:border-[var(--color-gold-2)] cursor-pointer transition"
                  style={{ boxShadow: 'var(--shadow-xs)' }}
                >
                  <summary className="flex items-center justify-between gap-3 list-none text-sm sm:text-base font-bold text-[var(--color-ink)]">
                    <span className="flex-1 min-w-0 break-words leading-snug">{q}</span>
                    <span className="text-2xl shrink-0 transition-transform group-open:rotate-45 text-[var(--color-gold-3)]" aria-hidden="true">
                      +
                    </span>
                  </summary>
                  <p className="mt-4 leading-relaxed text-[var(--color-ink-soft)] text-sm">
                    {a}
                  </p>
                </details>
              ))}
            </div>
          </section>
        ))}
      </div>

        <Cta href="/order" label="بقي لديك سؤال؟ تواصل معنا" />
      </PageContainer>
    </PageShell>
  );
}
